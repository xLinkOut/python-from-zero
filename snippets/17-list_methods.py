array = []      	# Lista vuota
print(array)
array.append(1)   	# [1]
print(array)
array.insert(3,4)   # [1,4]
print(array)
array.insert(1,7)   # [1,7,4]
print(array)
array.reverse()   	# [4,7,1]
print(array)
array.sort()    	# [1,4,7]
print(array)
print(min(array))   # Valore minimo
print(max(array))   # Valore massimo
